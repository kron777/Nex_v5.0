def write_state_json(snap_ts):
    """Write the runtime state snapshot to snapshots/."""
    SNAP_DIR.mkdir(parents=True, exist_ok=True)
    fname = datetime.fromtimestamp(snap_ts).strftime("%Y-%m-%d_%H%M") + "_state.json"
    path = SNAP_DIR / fname

    state = {
        "snapshot_at": snap_ts,
        "snapshot_at_str": datetime.fromtimestamp(snap_ts).strftime("%Y-%m-%d %H:%M:%S"),
        "runtime": ex.extract_runtime_state(),
        "port_status_counts": _port_counts(ex.extract_port_status()),
        "fountain_by_branch_24h": ex.extract_runtime_state().get("fountain_24h_by_branch", {}),
    }
    path.write_text(json.dumps(state, indent=2, default=str))
    return path


def _port_counts(port_data):
    """Normalize status strings and count."""
    if not port_data or "rows" not in port_data:
        return {}
    counts = {}
    for row in port_data["rows"]:
        status = row.get("status", "")
        if "DONE" in status:
            key = "DONE"
        elif "UNBLOCKED" in status:
            key = "UNBLOCKED"
        elif "QUEUED" in status:
            key = "QUEUED"
        elif "DESIGN-COMPLETE" in status:
            key = "DESIGN-COMPLETE"
        elif "DEFERRED" in status:
            key = "DEFERRED"
        else:
            key = "OTHER"
        counts[key] = counts.get(key, 0) + 1
    return counts


